    for chunk in chunks:
        summarized_chunk = summarize_text(chunk)
        print(summarized_chunk)
        summarized_chunks.append(summarized_chunk) 

    concatenated_summaries = "\n".join(summarized_chunks)
    final_summary = summarize_text(concatenated_summaries)
